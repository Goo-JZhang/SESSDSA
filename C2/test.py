    astack=[]
    result=[]
    i=0
    while i<len(tune):
        astack.append(tune[i])
        if astack[-1]=='(':
            astack.pop()
            i=i+1
            while tune[i]!=')':
                result.append(tune[i]+tune[i+1]+':'+astack[0])
                i=i+2
            astack.pop()
        i=i+1
    return result

